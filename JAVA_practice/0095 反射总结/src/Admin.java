
public class Admin {
	//Field
	private int id;	
	private String name;
	//Method
	
	public Admin()
	{
		System.out.println("Admin");
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	//Constructor
}
